# Task #1:
# There is a list filled with random integers, get a new list, the elements of which will be
# squared elements of the source list
# [1, 2, 4, 0] -> [1, 4, 16, 0]

import random
import re

lst = [random.randint(1,10) for _ in range(10)]
print(lst)
lst_res = [el**2 for el in lst]
print(lst_res)


# Task #2:
# There are two lists of fruits. Get a list of fruits that are in both source lists.

fruits_1 = ['orange', 'apricot', 'peach', 'apple', 'pear', 'plum']
fruits_2 = ['pear', 'fig', 'apricot', 'plum']
print(fruits_1)
print(fruits_2)
fruits_res = [el for el in fruits_1 if el in fruits_2]
print(fruits_res)


# Task #3:
# There is a list filled with unwanted numbers. Get a list of the elements from the original, satisfying following conditions:
# + Element multiple of 3
# + Item is positive
# + Element is not a multiple of 4

lst = [random.randint(-10,10) for _ in range(10)]
print(lst)
lst_1 = [el for el in lst if el%3==0]
print(lst_1)
lst_2 = [el for el in lst if el>=0]
print(lst_2)
lst_4 = [el for el in lst if el%4!=0]
print(lst_4)


